package domain.Ymir;

public class InfiniteVoid {
	boolean isActivated;
	boolean frozeEverything;

	public InfiniteVoid() {
			
	}

	public boolean isActivated() {
		return isActivated;
	}

	public void setActivated(boolean isActivated) {
		this.isActivated = isActivated;
	}

	public boolean isFrozeEverything() {
		return frozeEverything;
	}

	public void setFrozeEverything(boolean frozeEverything) {
		this.frozeEverything = frozeEverything;
	}
	
}